public class TipoVariable {
    public static void main(String[] args){
        int edad = 27;                                  // Variable tipo entero.
        System.out.println(edad);

        edad = 47;                                      // Asignar un nuevo valor a la variable.
        System.out.println("Su edad es " + edad);       // Concatenacion.

        edad = 47 - 27;                                 // Funciona con operaciones matematicas.
        System.out.println("Su edad ahora es " + edad);

        double salario = 1500.5;                        // Variable tipo decimal.
        System.out.println("Su salario es " + salario);   
    }
}
